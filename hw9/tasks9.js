// 1 задание Создать приложение, которое помогает пользователю выбрать напиток в зависимости от времени суток.

// Запрос времени суток у пользователя
const timeOfDay = prompt("Введите время суток (утро, день, вечер):");

// Проверка корректности ввода
if (typeof timeOfDay !== "string" || timeOfDay.trim() === "") {
  alert("Ошибка: введите корректное время суток (утро, день, вечер).");
} else {
  // Нормализуем строку для проверки
  const normalizedTime = timeOfDay.trim().toLowerCase();

  // Выбор напитка в зависимости от времени суток
  let drink;
  if (normalizedTime === "утро") {
    drink = "кофе";
  } else if (normalizedTime === "день") {
    drink = "чай";
  } else if (normalizedTime === "вечер") {
    drink = "горячий шоколад";
  } else {
    alert("Ошибка: введите одно из значений — утро, день или вечер.");
  }

  // Вывод результата, если напиток выбран
  if (drink) {
    alert(`Мы рекомендуем вам ${drink}.`);
  }
}


